function zoomIn() {
	zoom += zoomStep;
	updateZoom();
}

function zoomOut() {
	zoom -= zoomStep;
	if (zoom < zoomStep) {
		zoom = zoomStep;
	}
	updateZoom();
}

function updateZoom() {
	const contentElement = document.querySelector('#logo-groups');
	const zoomLevelElement = document.querySelector('.zoom-indicator');
	contentElement.style.transform = 'scale(' + zoom + ')';
	zoomLevelElement.textContent = (zoom * 100).toFixed(0) + '%';
}

function throttle(func, limit) {
	var inThrottle;
	return function () {
		const args = arguments;
		const context = this;
		if (!inThrottle) {
			func.apply(context, args);
			inThrottle = true;
			setTimeout(function () {
				inThrottle = false;
			}, limit);
		}
	};
}

